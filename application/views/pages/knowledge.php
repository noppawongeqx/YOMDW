<br><br>
<div class="headdata">นักลงทุนที่เหมาะสมกับการลงทุน DW </div>
<hr size="2" color="#999999">

<ol>
	<li>ชอบลงทุนใช้เงินน้อย แต่ได้ลุ้นเยอะ : DW ถือว่าเป็นผลิตภัณฑ์ที่ใช้เงินลงทุนไม่มากเมื่อเที่ยบกับผลตอบแทนที่ได้รับ ในที่นี้ผลตอบแทนหมายถึงนักลงทุนมีโอกาสได้รับกำไรเยอะๆ หรือขาดทุนเยอะได้เช่นกัน </li>
	<li>ชอบซื้อขายทั้งขาขึ้น และขาลง : จุดเด่นที่สำคัญของ DW คือสามารถให้นักลงทุนสามารถทำกำไรได้ คือ Call DW เหมาะสม ในสภาวะตลาดขาขึ้น และ Put DW เหมาะสมในสภาวะตลาดขาลง </li>
	<li>ชอบค่าคอมมิสชั่นถูกๆ : ราคา DW มักจะต่ำกว่ามูลค่าตลาดของหุ้นอ้างอิงมาก เช่น DW ราคา 5 บาท ราคาหุ้นอ้างอิงต่อ 1 DW อยู่ที่ 50 บาท แปลว่า นักลงทุนจ่ายค่าคอมมิสชั่นในการซื้อ DW น้อยลง 10 เท่าเมื่อเทียบกับการซื้อขายหุ้นอ้างอิง	</li>
</ol>


