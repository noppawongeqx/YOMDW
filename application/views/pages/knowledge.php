<div class="headdata">ประโยชน์และความเสี่ยงของ DW</div>
<hr size="2" color="#999999">

<div><strong>ประโยชน์ของ DW</strong></div>
<ul>
	<li>ใช้เงินลงทุนน้อยกว่าการซื้อขายหุ้นอ้างอิงโดยตรง</li>
	<li>เป็นผลิตภัณฑ์ที่มีอัตราทด (Gearing) สามารถทำกำไรได้มากแม้หุ้นอ้างอิงจะเปลี่ยนแปลงไป	เพียงเล็กน้อย</li>
	<li>สามารถจำกัดผลการขาดทุนได้ แต่มีโอกาสทำกำไรได้ไม่จำกัด</li>
	<li>สามารถทำกำไรได้ ไม่ว่าราคาหุ้นอ้างอิงจะปรับตัวขึ้นหรือลง</li>
	<li>มีสภาพคล่องสูงเนื่องจากมีผู้ดูแลสภาพคล่องทำให้นักลงทุนสามารถซื้อขาย DW ได้ตามที่	ต้องการ</li>
	<li>ไม่ต้องวางหลักประกัน </li>
</ul>

<div><strong>ความเสี่ยงของ DW</strong></div>
<ul>
	<li>ไม่ซื้อ DW ที่ราคาต่ำกว่า เพราะเชื่อว่าถูกกว่า : การเปรียบเทียบว่า DW ตัวใดถูกกว่า ควรพิจารณาความผันผวนแฝง (Implied Volatility) DW ตัวใดมีความผันผวนแฝงน้อยกว่านั้นถือว่าถูกกว่า แต่ในทางปฏิบัติ DW ที่ได้รับความนิยมอย่างไม่ได้มีความผันผวนแฝงน้อยที่สุดก็ได้ เพราะไม่ใช่ทุกคนที่จะใช้ความผันผวนแฝงเป็นปัจจัยหลักในการตัดสินใจ </li>
	<li>หลีกเลี่ยงการซื้อ DW ที่มีราคาต่ำกว่า 0.10 บาท : นักลงทุนบางคนอาจชอบซื้อ DW ราคาต่ำๆ เช่น 1 สตางค์ เพราะคิดว่าถ้าราคาปรับเพียง 1 ช่วงราคา จะทำให้ตนได้รับกำไรทันที 100% ซึ่งเหตุการณ์นี้มีโอกาสเกิดได้แต่น้อยมาก ทำให้นักลงทุนอาจขาดทุนเม็ดเงินลงทุนทั้งหมด อีกทั้งเมื่อราคา DW ต่ำกว่า 5 สตางค์ ผู้ดูแลสภาพคล่องไม่จำเป็นต้องรับซื้อคืนแล้ว อาจทำให้ราคาของ DW นั้นลดลงอย่างรวดเร็ว </li>
	<li>อย่าซื้อแบบถัวเฉลี่ย : เพราะเมื่อเวลาผ่านไปราคา DW จะค่อยๆ ลดลงอันเนื่องจากการเสื่อมค่าทางเวลา </li>
	<li>ไม่ถือ DW นานเกินไป : ราคาของ DW จะค่อยๆลดลงตามการเสื่อมค่าของเวลา แม้ว่านักลงทุนจะลงทุนถูกทาง และควรได้กำไร แต่ถ้าถือ DW ไว้เป็นระยะเวลานานเกินไป ผลของเวลาอาจทำให่กลายเป็นขาดทุนได้</li>
	<li>ไม่ควรถือ DW จนครบกำหนดอายุ : เนื่องจากนักลงทุนจะเสียภาษีเงินได้ตามประมวลรัษฎากร มาตรา40(8) หรือภาษีเงินได้ประเภทที่ 8 เว้นแต่ว่าจะมีกรณีที่ขายไม่ได้ เพราะไม่มีผู้รับซื้อคืนอันเนื่องจากว่าราคาใกล้ศูนย์ ในกรณีนี้นักลงทุนจะขาดทุนเงินลงทุนทั้งหมดแทน </li>
</ul>

