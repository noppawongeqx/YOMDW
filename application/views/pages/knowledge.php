<div class="headdata">รู้จัก DW</div>
<hr size="2" color="#999999">

<ul>
	<li>DW = Derivative Warrant (ใบสำคัญแสดงสิทธิอนุพันธ์)</li>
	<li>DW เป็นหลักทรัพย์ จดทะเบียนและซื้อขายในตลาดหลักทรัพย์ (SET)</li>
	<li>ผู้ออกเป็นบริษัทหลักทรัพย์หรือธนาคาร ไม่ใช่เจ้าของหุ้นอ้างอิง</li>
	<li>คุณสมบัติเหมือนสัญญาอนุพันธ์ (Call/Put)</li>
	<li>ซื้อขายเหมือนหุ้น (ขั้นต่ำ 100 หน่วย) และค่าคอมมิชชั่นเท่ากับหุ้น</li>
	<li>มีผู้ดูแลสภาพคล่อง (Market Maker)</li>
	<li>ปัจจุบันไม่สามารถแลกเป็นหุ้นอ้างอิงได้เมื่อถึงวันครบกำหนดอายุ</li>
</ul>

