<a name="01"></a>
<div class="headdata">เหตุใด DW บางตัวจึงมีแต่ Call DW ไม่ออกมาทั้ง Call และ Put</div>
<hr size="2" color="#999999">
<p>เพราะการออก DW คำนึงถึงความต้องการของนักลงทุนในตลาด ซึ่งโดยมากแล้วมีความต้องการฝั่ง Call มากกว่า Put และคำนึงถึงทิศทางราคาหุ้นอ้างอิงประกอบ </p>

<a name="02"></a>
<br>
<div class="headdata">ผู้ออกเสนอขาย DW ได้กำไรจากนักลงทุนเท่ากับราคาที่ขาย DW ใช่หรือไม่</div>
<hr size="2" color="#999999">
<p><strong>ไม่ใช่</strong> เพราะเงินที่ได้จากการขาย DW ให้นักลงทุน ผู้ออกจะต้องนำไปใช้ในการป้องกันความเสี่ยง ดังนั้น “กำไรของผู้ออกเกิดจากการบริหารความเสี่ยงที่ดี”  </p>

<a name="03"></a>
<br>
<div class="headdata">ทำไมราคา DW ไม่ขยับตาม เมื่อราคาหุ้นอ้างอิงขยับแล้ว</div>
<hr size="2" color="#999999">
<p>เนื่องจาก DW แต่ละตัวมีความอ่อนไหวต่อการเปลี่ยนแปลงราคาเมื่อเทียบกับการเปลี่ยนแปลงราคาของหุ้นอ้างอิง (Delta) แตกต่างกัน โดยขึ้นกับสถานะของ DW และอายุคงเหลือ โดย DW ที่มีสถานะ Out of The Money จะมีความอ่อนไหวต่อการเปลี่ยนแปลงราคาต่ำกว่า DW ที่มีสถานะ In The Money และเมื่ออายุคงเหลือลดน้อยลง DW ที่มีสถานะ Out of the Money จะมีความอ่อนไหวต่อการเปลี่ยนแปลงราคาลดลง ส่วน DW ที่มีสถานะ In the Money จะมีความไวต่อการเปลี่ยนแปลงราคาสูงขึ้น </p>

<a name="04"></a>
<br>
<div class="headdata">นักลงทุนควรถือ DW ไปจนครบกำหนดอายุใช่หรือไม่</div>
<hr size="2" color="#999999">
<p><strong>ไม่ใช่ </strong> เพราะ DW ที่มีสถานะ In the Money เงินสดส่วนต่างที่นักลงทุนจะได้รับจะถูกหักภาษี แต่การขายคืนในกระดานจะได้รับการยกเว้นภาษี เหมือนการซื้อขายหุ้นปกติ แต่ถ้า DW ที่มีสถานะ Out of the Money มูลค่าจะเข้าใกล้เป็นศูนย์ เมื่ออายุคงเหลือน้อยลง</p>

<a name="05"></a>
<br>
<div class="headdata">DW ที่ไม่มีการซื้อขายจากนักลงทุน จัดเป็น DW ที่ไม่มีสภาพคล่อง ไม่ควรลงทุนใช่หรือไม่</div>
<hr size="2" color="#999999">
<p><strong>ไม่ใช่</strong> Market Maker จะส่งคำสั่งซื้อขาย โดยใช้ราคาหุ้นอ้างอิงมาคำนวณ และมีการส่งคำสั่งเพื่อเปลี่ยนแปลงราคาให้สอดคล้องกับราคาหุ้นอ้างอิง ทำให้นักลงทุนสามารถทำการซื้อขายได้ตลอดเวลา</p>

<a name="06"></a>
<br>
<div class="headdata">นักลงทุนที่ซื้อ Call หรือ Put DW ไปแล้วต้องรอให้ราคาหุ้นอ้างอิงในตลาดสูงหรือต่ำกว่าราคาใช้สิทธิ จึงจะได้กำไร ใช่หรือไม่</div>
<hr size="2" color="#999999">
<p><strong>ไม่ใช่</strong> เพราะราคา Call หรือ Put DW จะเพิ่มขึ้น ก็ต่อเมื่อราคาหุ้นอ้างอิงเพิ่มขึ้นหรือลดลง กำไร/ขาดทุนของนักลงทุนจะขึ้นอยู่กับการคาดการณ์ราคาหุ้นอ้างอิงในอนาคต โดยเทียบกับราคาตลาด ณ ปัจจุบัน ไม่ใช่เทียบกับราคาใช้สิทธิ</p>

<a name="07"></a>
<br>
<div class="headdata">ลงทุนใน DW ดีกว่าซื้อหุ้น เนื่องจาก DW จำกัดขาดทุนแต่สามารถทำกำไรไม่จำกัดใช่หรือไม่ </div>
<hr size="2" color="#999999">
<p><strong>ใช่</strong> เพราะว่านักลงทุนที่ลงทุนใน DW จะไม่มีผลขาดทุนเพิ่มขึ้น ถ้าราคาหุ้นอ้างอิง ลดลงต่ำกว่าราคาใช้สิทธิ ณ วันหมดอายุ แต่นักลงทุนที่ลงทุนในหุ้นอ้างอิงโดยตรงจะมีผลขาดทุนเพิ่มขึ้นตามราคาหุ้นอ้างอิงที่ลดลง ในที่นี้การจำกัดการขาดทุนนั้นจะจำกัดที่ราคา DW ที่นักลงทุนจ่ายไปในตอนแรกทั้งหมด ซึ่งก็อาจหมายถึงเงินลงทุนทั้งหมดนั่นเอง เพราะฉะนั้นนักลงทุนที่เหมาะสมที่จะลงทุนใน DW จึงจำเป็นต้องมีวินัยในการลงทุนที่ดี โดยจะต้องกำหนดจุดตัดขาดทุน (Price Stop Loss) และระยะเวลาที่ตัดขาดทุน (Time Stop Loss) </p>

<a name="08"></a>
<br>
<div class="headdata">ราคา DW เปรียบเทียบกับราคาหุ้นอ้างอิง สามารถสะท้อนความถูกหรือแพงของ DW ได้ใช่หรือไม่ </div>
<hr size="2" color="#999999">
<p><strong>ไม่ใช่</strong> เพราะ DW ที่มีอายุคงเหลือต่างกันมากๆ การเปรียบเทียบราคาของ DW กับราคาตลาดของหุ้นอ้างอิง (All in Premium) ไม่เหมาะกับการพิจารณาความถูกหรือแพงสำหรับตราสารที่เน้นการลงทุนระยะสั้นๆ เช่น DW ซึ่งหากต้องการเปรียบเทียบนักลงทุนควรพิจารณา Effective Gearing เทียบกับ Time Decay มากกว่า</p>

<a name="09"></a>
<br>
<div class="headdata">หากซื้อ DW ที่มีสถานะ Out-The-Money (OTM) นักลงทุนจำเป็นต้องขาดทุนเสมอใช่หรือไม่</div>
<hr size="2" color="#999999">
<p><strong>ไม่ใช่</strong> ในกรณีที่นักลงทุนไม่ได้คิดที่จะถือ DW ไปจนครบกำหนดอายุ  เนื่องจากตัวแปรที่ทำให้เกิดกำไร/ขาดทุนของนักลงทุนจะขึ้นอยู่กับการคาดการณ์ราคาเป้าหมายในอนาคตของหุ้นอ้างอิง และระยะเวลาเวลาการลงทุนของนักลงทุนเป็นหลัก ซึ่งข้อดีของ DW ที่ Out the Money คือมี Effective Gearing ที่ค่อนข้างสูง และใช้เงินลงทุนน้อยกว่า DW ที่ In the Money ที่มีหุ้นอ้างอิงและอายุคงเหลือเท่ากัน ส่วนข้อเสีย คือ ราคา DW จะเปลี่ยนแปลงช้ากว่าเมื่อเทียบกับการเปลี่ยนของราคาหุ้นอ้างอิง และมี Time Decay ต่อวันที่สูงกว่า ดังนั้นนักลงทุนจึงควรเพิ่มความระมัดระวังในการลงทุนโดยเฉพาะ DW ที่ Out the Money มากๆ และใกล้ครบกำหนดอายุ</p>

<a name="10"></a>
<br>
<div class="headdata">นักลงทุนไม่ควรลงทุนใน DW ที่ใกล้หมดอายุแล้วใช่หรือไม่</div>
<hr size="2" color="#999999">
<p><strong>ไม่ใช่</strong> ในกรณีที่ DW ที่มีสถานะ In the Money มากๆ เพราะราคาของ DW จะมีความอ่อนไหวต่อการเปลี่ยนแปลงของหุ้นอ้างอิงและ Effective Gearing ที่สูงในขณะที่ Time Decay ต่อวันค่อนข้างต่ำ</p>

<p><strong>ใช่</strong> ในกรณีที่ DW มีสถานะ Out the Money เนื่องจากความอ่อนไหวต่อการเปลี่ยนของหุ้นอ้างอิงจะค่อนข้างต่ำ และมี Time Decay ต่อวันที่สูงกว่า</p>

<a name="11"></a>
<br>
<div class="headdata">DW ที่ไม่มีปริมาณการซื้อขายถือว่า DW ตัวนั้นไม่มีสภาพคล่องใช่หรือไม่</div>
<hr size="2" color="#999999">
<p><strong>ไม่ใช่</strong> เพราะ DW ทุกตัวจะมีผู้ทำหน้าที่เป็น Market Maker ทำหน้าที่ส่งคำสั่งเสนอซื้อ-ขายเพื่อให้นักลงทุนสามารถทำการซื้อขายได้ตลอดเวลา เมื่อราคาหุ้นอ้างอิงมีการเปลี่ยนแปลง Market Maker ก็จะเป็นผู้ทำการเปลี่ยนแปลงราคาเสนอซื้อ-ขาย DW ให้สอดคล้องกับราคาหุ้นอ้างอิง</p>


